pub fn tri_vertex_quadrature<T>() -> (Vec<T>, Vec<Point2<T>>)
where
    T: RealField,
{
    let x: Vec<_> = vec![-1.0, 1.0, -1.0]
        .into_iter()
        .map(|t| T::from_f64(t).unwrap())
        .collect();
    let y: Vec<_> = vec![-1.0, -1.0, 1.0]
        .into_iter()
        .map(|t| T::from_f64(t).unwrap())
        .collect();
    let w = vec![1.0 / 3.0, 1.0 / 3.0, 1.0 / 3.0]
        .into_iter()
        .map(|t| T::from_f64(t).unwrap())
        .collect();

    let p = x
        .into_iter()
        .zip(y.into_iter())
        .map(|(x, y)| Point2::new(x, y))
        .collect();

    (w, p)
}

